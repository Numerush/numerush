<p>Kebijakan Privasi</p>
<p>
Kebijakan privasi Titipyuk ini dirancang untuk membantu pengguna mengetahui cara penggunaan, pengolahan, penanganan data pribadi pengguna yang telah dipercayakan kepada Titipyuk selama penyediaan jasa Titipyuk kepada pengguna. 
Titipyuk berkomitmen untuk menjaga dan melindungi privasi pengguna. Bila Titipyuk meminta pengguna untuk memberikan informasi tertentu ketika mengakses platform titipyuk, maka informasi tersebut hanya akan digunakan sesuai dengan pernyataan privasi ini.
Titipyuk dari waktu ke waktu memiliki hak untuk mengubah kebijakan privasi ini agar tetap relevan dengan kondisi terkini baik melakukan pengurangan atau penambahan pada ketentuan halaman ini. Pengguna dianjurkan untuk melihat halaman ini secara berkala agar pengguna megetahui perubahan-perubahan yang terjadi. Dengan menggunakan layanan pada TitipYuk, pengguna dianggap telah menyetujui perubahan ketentuan pada kebijakan privasi ini.
Kebijakan Privasi ini merupakan bagian tidak terpisahkan dan merupakan satu kesatuan dengan Aturan Penggunaan Titipyuk.
Data pribadi yang diminta dari pengguna
</p>

<p>
Titipyuk memiliki wewenang untuk meminta informasi pribadi dari pengguna. Informasi pribadi sebagaimana dimaksud adalah seperti :
</p>
<ul>
    <li>Nama lengkap</li>
    <li>Username dan password</li>
    <li>Jenis kelamin</li>
    <li>Alamat surat elekronik (e-mail)</li>
    <li>Nomor induk kependudukan</li>
    <li>No telefon</li>
    <li>Alamat lengkap</li>
    <li>Tanggal/ Bulan/ Tahun Lahir</li>
    <li>Informasi transaksi, keuangan, dan/atau perbankan</li>
    <li>Gambar wajah atau bagian tubuh manusia lainnya </li>
    <li>Data transaksi seperti rincian pembayaran terkait layanan saat menggunakan aplikasi Titipyuk</li>
</ul>

<p>
    Titipyuk mengasumsikan bahwa segala informasi beserta perubahannya yang disampaikan adalah informasi yang benar, akurat dan dapat dipertanggungjawabkan. Apabila perubahan informasi yang pengguna berikan tidak benar ataupun tidak mengganti perubahan, maka Titipyuk tidak bertanggungjawab atas segala akibat yang dapat terjadi.
    Titipyuk tidak akan meminta informasi yang sensitif seperti kesehatan, agama, ras, etnis, catatan criminal, perkumpulan organisasi. Jika pengguna melakukan penghapusan akun maka kami akan melakukan penghancuran data dengan mengacu pada ketentuan perundang-undangan yang berlaku.
</p>

